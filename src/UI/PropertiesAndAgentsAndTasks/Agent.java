    private void SaveAgentsButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveAgentsButtonActionPerformed
        /////////////// Getting input //////////////////////
        int numOfAgents;
        try {
            numOfAgents = Integer.parseInt(jTextField1.getText());
        } catch (NumberFormatException e) {
            System.out.println("XXX Number of Agents XXX");
            return;
        }
        int HemoLevel = -1;// -1:unknownYet, 0:AllHemo, 1:Groups, 2:AllHetero
        if (RadioButtonHemo.isSelected()) {
            HemoLevel = 0;
        } else if (RadioButtonGroups.isSelected()) {
            HemoLevel = 1;
        } else {
            if (RadioButtonHetro.isSelected()) {
                HemoLevel = 2;
            }
        }
        if (HemoLevel == -1) {
            System.out.println("XXX No choise for Hemo XXX");
            return;
        }
        /////////////// Updateing Num of Agents & Hemo //////////////////////
        if (Noun.getHemogeniousValue() == -1)// 1st time to click.
        {
            Modules.Agent.setHemogenious(HemoLevel);
            Modules.Agent.setNumberOfAgents(numOfAgents);
            Modules.Agent.setAgentsGroups(Modules.Agent.CreateListOfAgents(numOfAgents, HemoLevel));
        } else { // not the 1st time to click
            if (Modules.Agent.getNumberOfAgents() != numOfAgents) {// if number of agents changed
                if (Modules.Agent.getNumberOfAgents() > numOfAgents) {
                    System.out.println("WWW remove agents data may lose WWW");
                    //?return
                }
                Modules.Agent.changeNumberOfAgents(numOfAgents);
            }
            if (Noun.getHemogeniousValue() != HemoLevel) {//if hemo number of agents changed
                Noun.changeHemogenious(HemoLevel);
            }

        }
        SaveAgentsButton.setText("Saved");
        SaveAgentsButton.setEnabled(false);
        addHemogenetyPane(HemoLevel);
        AgentsTree.refreshTree();
    }//GEN-LAST:event_SaveAgentsButtonActionPerformed

