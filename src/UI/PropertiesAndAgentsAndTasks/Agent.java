    private void ChangeButtonNameSaveUpdate(int i) {
        if (Noun.getHemogeniousValue() == -1) {
            return;
        }
        try {
            if (Noun.getHemogeniousValue() != i || Integer.parseInt(jTextField1.getText()) != Modules.Agent.getNumberOfAgents()) {
                SaveAgentsButton.setText("Update");
                SaveAgentsButton.setEnabled(true);
            } else {
                SaveAgentsButton.setText("Saved");
                SaveAgentsButton.setEnabled(false);
            }
        } catch (NumberFormatException e) {
            return;
        }
    }

